SELECT
    INCPRVLG.HN,
    INCPRVLG.AN,
    INCPRVLG.PTTYPE,
    PTTYPE.NAME AS PTTYPENAME,
    INCPRVLG.PTTYPEEXT,
    PTTYPEEXT.NAME AS PTTYPEEXTNAME,
    INCPRVLG.PATIENTTYPE,
    INCPRVLG.SUBTYPE,
    INCPRVLG.ISSDATE,
    INCPRVLG.EXPDATE,
    INCPRVLG.CHKDATE,
    INCPRVLG.FIRSTDATE

FROM {{ source('ddc_internal', 'INCPRVLG') }} INCPRVLG

LEFT JOIN {{ source('ddc_internal', 'PTTYPE') }} PTTYPE
    ON INCPRVLG.PTTYPE = PTTYPE.PTTYPE

LEFT JOIN {{ source('ddc_internal', 'PTTYPEEXT') }} PTTYPEEXT
    ON INCPRVLG.PTTYPEEXT = PTTYPEEXT.PTTYPEEXT

WHERE INCPRVLG.CANCELDATE IS NULL
